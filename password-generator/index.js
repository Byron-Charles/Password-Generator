const passwordOne = document.querySelector(".password-one");
const passwordTwo = document.querySelector(".password-two");
const passwordBtn = document.querySelector(".password-btn");
let password = "";

function generatePassword() {
  for (let i = 0; i < 32; i++) {
    let randomIndex = Math.floor(Math.random() * characters.length);
    password += characters[randomIndex];
  }
  passwordOne.textContent = password.slice(0, 15);
  passwordTwo.textContent = password.slice(16, 31);
  password = "";
}

passwordBtn.addEventListener("click", generatePassword);
